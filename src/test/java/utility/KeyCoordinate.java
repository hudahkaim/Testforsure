package utility;

public class KeyCoordinate {
	private int X;
	private int Y;
	
	public KeyCoordinate(int x, int y) {
		this.X = x;
		this.Y = y;
	}
	
	public void setX(int x) {
		this.X = x;
	}
	
	public int getX() {
		return this.X;
	}
	
	public void setY(int y) {
		this.Y = y;
	}
	
	public int getY() {
		return this.Y;
	}
	
	
}
